/**
 *  카테고리 : 변수
 */
public class Example02_1 {
	public static void main(String[] args) {
		int year = 0;
		int age = 14;
		
		System.out.println(year);
		System.out.println(age);
		
		year = age + 2000;
		age = age + 1;
		
		System.out.println(year);
		System.out.println(age);
		
		System.out.println(year + 5);
		System.out.println(age + 1);
	}
}